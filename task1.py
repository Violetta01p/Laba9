def calculate_cosmic_distance():
   # Запитуємо у користувача частку швидкості світла (0-1)
   speed_of_light_fraction = float(input("Введіть частку швидкості світла (0-1): "))
   # Запитуємо час у роках
   time_years = float(input("Введіть час у роках: "))
   # Повертаємо результат як частку швидкості світла, помножену на час у роках
   return speed_of_light_fraction * time_years

# Функція для розрахунку спрощеного показника гравітації
def calculate_simplified_gravity():
   # Запитуємо маси двох об'єктів
   mass1 = float(input("Введіть масу першого об'єкта (кг): "))
   mass2 = float(input("Введіть масу другого об'єкта (кг): "))
   # Повертаємо результат як добуток мас двох об'єктів
   return mass1 * mass2
  
# Функція для розрахунку наближення сповільнення часу
def calculate_time_dilation_approximation():
   # Запитуємо частку швидкості світла (0-1)
   speed_of_light_fraction = float(input("Введіть частку швидкості світла (0-1): "))
   # Запитуємо час у секундах
   time_seconds = float(input("Введіть час у секундах: "))
   # Повертаємо наближене значення сповільнення часу за допомогою формули
   return time_seconds / (1 - speed_of_light_fraction)

# Головна функція програми
def main():
   # Показуємо користувачеві доступні опції для вибору
   print("Виберіть, яку 'таємницю Всесвіту' ви хочете розрахувати:")
   print("1 - Космічна відстань")
   print("2 - Спрощена гравітація")
   print("3 - Наближення сповільнення часу")


   # Запитуємо вибір користувача
   choice = input("Ваш вибір: ")

   # В залежності від вибору викликаємо відповідну функцію
   if choice == "1":
       result = calculate_cosmic_distance()
       # Виводимо результат для космічної відстані
       print(f"Приблизна космічна відстань: {result} світлових років.")

   elif choice == "2":
       result = calculate_simplified_gravity()
       # Виводимо результат для спрощеної гравітації
       print(f"Спрощений показник гравітаційної взаємодії: {result}.")

   elif choice == "3":
       result = calculate_time_dilation_approximation()
       # Виводимо результат для сповільнення часу
       print(f"Приблизне сповільнення часу: {result} секунд.")

   else:
       # Якщо вибір не співпав з жодним варіантом, виводимо повідомлення про помилку
       print("Невірний вибір.")
       return
   # Після розрахунку дякуємо користувачу за використання програми
   print("Дякуємо за використання калькулятора!")

# Запуск програми
if __name__ == "__main__":
   # Викликаємо головну функцію
   main()
